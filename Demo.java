public class Demo
{
    public static void main(String[]args){
        //while
        //for
        //for-each(not covered until arrays)
        //do while(not covered)
        
        int i=0;
        while(i<10){
            System.out.println("i=" + i);
            ++i;
        }
        for(int x=0; x<10; ++x){
            System.out.println("x = " + x);
        }
    }
}